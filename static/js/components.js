// Spinner component
export function Spinner(message = 'Carregando...') {
    return `
        <div class="text-center">
            <div class="animate-spin rounded-full h-16 w-16 border-t-2 border-b-2 border-primary-500 mx-auto mb-4"></div>
            <p class="text-white text-lg">${message}</p>
        </div>
    `;
}

// Button component
export function Button({ text, onClick, className = '', disabled = false, icon = null }) {
    const id = 'btn-' + Math.random().toString(36).substr(2, 9);
    setTimeout(() => {
        const btn = document.getElementById(id);
        if (btn && onClick) btn.addEventListener('click', onClick);
    }, 0);

    return `
        <button 
            id="${id}" 
            class="px-4 py-2 rounded-lg font-bold transition-all ${className} ${disabled ? 'opacity-50 cursor-not-allowed' : ''}"
            ${disabled ? 'disabled' : ''}
        >
            ${icon ? `<span class="inline-block mr-2">${icon}</span>` : ''}
            ${text}
        </button>
    `;
}

// Card component
export function Card({ title, subtitle, onClick, className = '' }) {
    const id = 'card-' + Math.random().toString(36).substr(2, 9);
    setTimeout(() => {
        const card = document.getElementById(id);
        if (card && onClick) card.addEventListener('click', onClick);
    }, 0);

    return `
        <div id="${id}" class="p-4 bg-gray-800 rounded-xl border border-gray-700 hover:border-primary-500 transition-all cursor-pointer ${className}">
            <h3 class="font-semibold text-white mb-1">${title}</h3>
            ${subtitle ? `<p class="text-sm text-gray-400">${subtitle}</p>` : ''}
        </div>
    `;
}

// Progress bar
export function ProgressBar(current, total) {
    const percentage = total > 0 ? (current / total) * 100 : 0;
    return `
        <div class="w-full bg-gray-800 rounded-full h-4 mb-4">
            <div class="bg-primary-500 h-4 rounded-full transition-all duration-300" style="width: ${percentage}%"></div>
        </div>
    `;
}

// Helper to render app content
export function render(html) {
    const app = document.getElementById('app');
    if (app) app.innerHTML = html;
}
